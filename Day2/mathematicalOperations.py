'''
Mathematica Operations in Python
+ Addition 
- Subtraction
* Multiplication
/ Division (float output)
// Division (floor division - int output)
** Exponent
'''

print(7 + 4)
print(7 - 4)
print(7 * 4)
print(7 / 4)
print(7 // 4)
print(7 ** 4)

# Order of Operations

# P (Parenthesis)
# E (Exponent)
# M (Multiplication)
# D (Division)
# A (Addition)
# S (Subtraction)
# L (Left)
# R (Right)

print(3 * 3 + 3 / 3 - 3) # 7
print(3 * (3 + 3) / 3 - 3) # 3