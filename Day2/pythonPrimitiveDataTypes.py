# Python Primitive Data Type

'''
Data Types

Strings - A string of characters: "Hello"
Integer - Whole numbers: 12345
Float - Floating point numbers: 3.1415
Boolean - True or False

'''

# Subscripting, pulling out a char from a string
print("Hello"[0])
print("Hello"[4])
print("Hello"[-1])
print("123" + "345")

print(123 + 345)
print(123)
print(type(123))
print(123_456_789) 

print(3.1415)
print(type(3.1415))

print(True)
print(False)